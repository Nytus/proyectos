package pk1;

public class Ataque_j1{
	
	public int ataqueTotal(Jugador jugador1, Armas arma1, Armas arma2, Armas arma3){
		int suma = 0;
			if (arma1.usando = true) {
				jugador1.ataque = jugador1.ataque + arma1.ataque;
				suma = jugador1.ataque;
				}
			else if (arma2.usando = true) {
				jugador1.ataque = jugador1.ataque + arma2.ataque;
				suma = jugador1.ataque;
				}
			else if (arma3.usando = true) {
				jugador1.ataque = jugador1.ataque + arma3.ataque;
				suma = jugador1.ataque;
				}
			else {
				suma = jugador1.ataque;
			}
			return suma;
		
	}}
