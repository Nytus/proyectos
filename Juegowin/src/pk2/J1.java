package pk2;

public class J1 {
	
	
	
	private int vida;
	private int esp;
	private int dinero;
	private int ataque;
	
	public J1 (int vida,int esp, int dinero, int ataque) {
		this.vida = vida;
		this.esp = esp;
		this.dinero = dinero;
		this.ataque = ataque;		
	}

	public int getVida() {
		return vida;
	}

	public void setVida(int vida) {
		this.vida = vida;
	}

	public int getEsp() {
		return esp;
	}

	public void setEsp(int esp) {
		this.esp = esp;
	}

	public int getDinero() {
		return dinero;
	}

	public void setDinero(int dinero) {
		this.dinero = dinero;
	}

	public int getAtaque() {
		return ataque;
	}

	public void setAtaque(int ataque) {
		this.ataque = ataque;
	}
	
	
	
	

}
